% --- Executes just before MobiFit is made visible.
function MobiFit_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to MobiFit (see VARARGIN)
% set(hObject,'Units','pixels');
% figuresize=get(hObject,'Position');
% screensize=get(0,'screensize');
% set(gcf,'outerposition',[(screensize(3)-figuresize(3))/2,(screensize(4)-figuresize(4))/2,figuresize(3),figuresize(4)]);
movegui( gcf, 'center' )
% Choose default command line output for MobiFit
handles.output = hObject;

% Update handles structure
guidata(hObject, handles);

% UIWAIT makes MobiFit wait for user response (see UIRESUME)
% uiwait(handles.figure1);
global gloablcount;
global sGui;
global CellularALL;

ha=axes('units','normalized','pos',[0.12 0.12 0.8 0.4]);
uistack(ha,'down');
ii=imread('Mobfit.jpg');
%设置程序的背景图为beijing1.jpg
image(ii);
colormap gray
set(ha,'handlevisibility','off','visible','off');
% set(ha,'BackgroundColor','white');


set(handles.edit7,'String',gloablcount-2);
set(handles.edit7,'fontname','Times New Roman');
set(handles.edit8,'fontname','Times New Roman');
set(handles.edit9,'fontname','Times New Roman');
set(handles.edit10,'fontname','Times New Roman');
set(handles.edit11,'fontname','Times New Roman');
set(handles.edit12,'fontname','Times New Roman');
set(handles.text4,'fontname','Times New Roman');
set(handles.text5,'fontname','Times New Roman');
set(handles.text6,'fontname','Times New Roman');
set(handles.text7,'fontname','Times New Roman');
set(handles.text8,'fontname','Times New Roman');
set(handles.text10,'fontname','Times New Roman');
% set(handles.text2,'String','Current action')
% set(handles.text2,'FontSize',17)
% set(handles.text3,'String','Counts')
% set(handles.text3,'FontSize',17)
plot(handles.axes1,sGui,'blue');


axis tight;
set(gca,'FontSize',18);
set(gca, 'LineWidth',1,'fontname','Times New Roman');
xlabel('Sampling point','FontSize',18,'Fontname', 'Times New Roman');
ylabel('Amplitude','FontSize',18,'Fontname', 'Times New Roman');
set(gca,'FontSize',18)
set(gca, 'LineWidth',1.25)
hold on;
value1 = 1;
value2 = 1;
i = 2;
if gloablcount-2>0
%     set(handles.edit1,'String',"Squat");
    while value1~=0 &&value2~=0
        plot([CellularALL(1,i)*8+255,CellularALL(1,i)*8+255],[min(sGui),max(sGui)],'black','linewidth',3);
        plot([CellularALL(2,i)*8+255,CellularALL(2,i)*8+255],[min(sGui),max(sGui)],'black','linewidth',3);
        set(gca,'FontSize',18)
        set(gca, 'LineWidth',1.25)
        value1 = CellularALL(1,i+1);
        value2 = CellularALL(2,i+1);
        i=i+1;
    end
end

